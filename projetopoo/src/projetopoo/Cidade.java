package projetopoo;

public class Cidade {
    int idCidade;
    String nome;

    public void setIdCidade(int idCidade) {
        this.idCidade = idCidade;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdCidade() {
        return idCidade;
    }

    public String getNome() {
        return nome;
    }
}
