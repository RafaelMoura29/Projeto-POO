package projetopoo;

public class Estado {
    int idEstado;
    String nome;

    public void setIdEstado(int idEstado) {
        this.idEstado = idEstado;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdEstado() {
        return idEstado;
    }

    public String getNome() {
        return nome;
    }
}
